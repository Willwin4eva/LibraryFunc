#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char *sp;

	sp = NULL;
	while (1)
	{
		if (*s == c)
			sp = (char *)s;
		if (*s++ == 0)
			break ;
	}
	return (sp);
}
